# 文档操作
# 显示行号  :set nu  命令模式
# 保存并退出  :wq  命令模式
# 保存并退出  :x  命令模式

# 光标移动
# 光标左移n列  n_h  一般模式
# 光标右移n列  n_l  一般模式
# 光标上移n行  n_k  一般模式
# 光标下移n行  n_j  一般模式
# 光标移动到n行  n_G  一般模式
# 光标移动到首行  gg  一般模式
# 光标移动到结尾行  G  一般模式

# 翻页
# 上翻一页  Ctrl + b  一般模式
# 下翻一页  Ctrl + f  一般模式
# 到文件n%的位置  n_%  一般模式
# 下翻一页  Ctrl + f  一般模式

# 复制粘贴
# 复制整行  yy or Y  一般模式
# 剪切n行  n_dd  一般模式
# 粘贴  p  一般模式

# 查找替换
# 查找  /something  一般模式
# 查找上一个  N  一般模式
# 查找下一个  n  一般模式
# 用new替换当前行第一个old  :s/old/new  末行模式
# 用new替换当前行所有的old  :s/old/new/g  末行模式
# 用new替换文件n1行到n2行所有的old  :n1,n2s/old/new/g  末行模式
# 用new替换文件中所有的old  :%s/old/new/g  末行模式
# 在每一行的行首插入xxx  :%s/^/xxx/g  末行模式

# 撤销恢复
# 撤销  u  一般模式
# 恢复  Ctrl + r  一般模式

# 显示行号  :set nu  末行模式


